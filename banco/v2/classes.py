from abc import ABC, abstractmethod
from datetime import datetime


class ContasIterador:
    def __init__(self, contas):
        self.contas = contas
        self._index = 0

    def __iter__(self):
        return self

    def __next__(self):
        try:
            conta = self.contas[self._index]
            return f"""\
            Agência:\t{conta.agencia}
            Número:\t\t{conta.numero}
            Titular:\t{conta.cliente.nome}
            Saldo:\t\tR$ {conta.saldo:.2f}
        """
        except IndexError:
            raise StopIteration
        finally:
            self._index += 1


class Cliente:
    def __init__(self, endereco):
        self.endereco = endereco
        self.contas = []

    def realizar_transacao(self, conta, transacao):
        # O 'isinstance' verifica se o objeto 'transacao' é da classe 'Saque'
        if isinstance(transacao, Saque):

            # Filtra as transações de saque feitas no dia de hoje
            saques_hoje = [
                t for t in conta.historico.transacoes
                if t["tipo"] == Saque.__name__ and t["data"].date() == datetime.now().date()
            ]

            # Acessa o limite de saques da conta (que deve ser uma ContaCorrente)
            if len(saques_hoje) >= conta._limite_saques:
                print(
                    "\n@@@ Operação falhou! Você excedeu o número de saques permitidos para hoje! @@@")
                return  # Invalida a operação

        # Se a validação passar (ou não for um saque), registra a transação
        transacao.registrar(conta)

    def adicionar_conta(self, conta):
        self.contas.append(conta)


